export function tr(lang: Lang, key: string, params?: Record<string, string | number>): string {
  let s = I18N[lang]?.[key] ?? I18N.zh[key] ?? key;
  if (params) for (const k in params) s = s.replace(`{${k}}`, String(params[k]));
  return s;
}
